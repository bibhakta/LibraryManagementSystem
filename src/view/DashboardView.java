    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
     conn = MyConnection.dbconnect();
try {
    stmt = conn.createStatement();
    String name = txtname1.getText();
    String sql = "SELECT * FROM users WHERE name = '" + name + "'";
    ResultSet rs = stmt.executeQuery(sql);
    
    if (rs.next()) {
        // Retrieve the data from the result set
        String userId = rs.getString("user_id");
        String phoneNum = rs.getString("phone_num");
        String fatherName = rs.getString("father_name");
        String gmail = rs.getString("gmail");
        
        // Set the retrieved data in the respective text fields
        txtID.setText(userId);
        txtphone1.setText(phoneNum);
        txtfather1.setText(fatherName);
        txtgmail1.setText(gmail);
    } else {
        // Clear the text fields if user is not found
        txtID.setText("");
        txtphone1.setText("");
        txtfather1.setText("");
        txtgmail1.setText("");
        
        JOptionPane.showMessageDialog(rootPane, "User not found");
    }
} catch (Exception e) {
    // Handle any exceptions that occur during the search
}

    // Handle any exceptions

    }//GEN-LAST:event_btnsearchActionPerformed

